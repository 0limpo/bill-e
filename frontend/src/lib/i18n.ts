/**
 * Get a translation function for a specific language
 */
export const getTranslator = (lang: Language) => {
  return (key: string): string => {
    // Fallback chain: target language → English → Spanish → key.
    // English is preferred over Spanish as the secondary fallback because
    // it's more universally understood for languages with partial coverage
    // (currently ar and bn). Spanish remains as final fallback since it's
    // the canonical source for the original key set.
    return translations[lang][key] || translations.en[key] || translations.es[key] || key;
  };
};

/**
 * Detect browser language
 */
export const detectLanguage = (): Language => {
  if (typeof navigator === 'undefined') return 'es';
  const browserLang = navigator.language.slice(0, 2).toLowerCase();
  const supportedLanguages: Language[] = ['es', 'en', 'pt', 'zh', 'hi', 'fr', 'ru', 'ja', 'de', 'id', 'ar', 'bn'];
  if (supportedLanguages.includes(browserLang as Language)) {
    return browserLang as Language;
  }
  return 'es'; // Default to Spanish
};

/**
 * Detect language from an Accept-Language header (server-side use).
 * Picks the first supported language in the user's preference list.
 * Format: "en-US,en;q=0.9,es;q=0.8"
 */
export const detectLanguageFromHeader = (acceptLanguage: string | null | undefined): Language => {
  if (!acceptLanguage) return 'es';
  const supportedLanguages: Language[] = ['es', 'en', 'pt', 'zh', 'hi', 'fr', 'ru', 'ja', 'de', 'id', 'ar', 'bn'];
  const candidates = acceptLanguage
    .split(',')
    .map((s) => s.trim().split(';')[0].slice(0, 2).toLowerCase());
  for (const cand of candidates) {
    if (supportedLanguages.includes(cand as Language)) return cand as Language;
  }
  return 'es';
};
